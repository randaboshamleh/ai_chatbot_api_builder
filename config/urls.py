from django.contrib import admin
from django.urls import path, include, re_path
from django.conf import settings
from django.conf.urls.static import static
from django.http import JsonResponse
from rest_framework import permissions
from drf_yasg.views import get_schema_view
from drf_yasg import openapi

schema_view = get_schema_view(
    openapi.Info(
        title="AI Chatbot API",
        default_version='v1',
        description="API Documentation for AI-powered RAG Chatbot with Multi-tenant support",
        terms_of_service="https://www.example.com/terms/",
        contact=openapi.Contact(email="contact@example.com"),
        license=openapi.License(name="MIT License"),
    ),
    public=True,
    permission_classes=(permissions.AllowAny,),
)

urlpatterns = [
    path('admin/', admin.site.urls),
    path('healthz/', lambda request: JsonResponse({'status': 'ok'})),
    
    # API Documentation
    re_path(r'^swagger(?P<format>\.json|\.yaml)$', schema_view.without_ui(cache_timeout=0), name='schema-json'),
    path('swagger/', schema_view.with_ui('swagger', cache_timeout=0), name='schema-swagger-ui'),
    path('redoc/', schema_view.with_ui('redoc', cache_timeout=0), name='schema-redoc'),
    
    # API Endpoints
    path('api/v1/', include('apps.authentication.urls')),
    path('api/v1/', include('apps.documents.urls')),
    path('api/v1/', include('apps.chatbot.urls')),
    path('api/v1/', include('apps.tenants.urls')),
    path('api/v1/', include('apps.analytics.urls')),
    
    # Public Web Chat API (no authentication required)
    path('api/v1/webchat/', include('apps.chatbot.web_chat_urls')),
] + static(settings.MEDIA_URL, document_root=settings.MEDIA_ROOT)
